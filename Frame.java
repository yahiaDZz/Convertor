    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        int inp = 0;
        double d = 0;
        String s = txtDecimal.getText();
        if (!s.contains(".")) {
            try {
                inp = Integer.parseInt(txtDecimal.getText());
                reset();
            } catch (Exception ex) {
                error("invalid input...");
                reset();
            }

            txtCR.setText(String.valueOf(complement1(Math.abs(inp))));
            txtCV.setText(String.valueOf(complement2(Math.abs(inp))));
            txtSVA.setText((inp > 0 ? "0" : "1") + String.valueOf(toSysX(Math.abs(inp), 2)));
        } else {
            reset();
            try {
                d = Double.parseDouble(txtDecimal.getText());
            } catch (Exception ex) {
                error("invalid input...");
                reset();
            }
            txtFP.setText(fixedpoint(Math.abs(d), 2));
            txtFlP.setText(floatingPoint(d));
        }
    }//GEN-LAST:event_btnShowActionPerformed

