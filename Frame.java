    static String fixedpoint(double n, int x) {
        StringBuilder sb = new StringBuilder();
        double r = n % 1;
        double l = n - r;
        sb.append(String.valueOf(toSysX((int) l, x)) + ".");
        Map<Double, Integer> map = new HashMap<>();
        map.put(r, 1);
        while (map.get(r) != 2 && sb.length() < 24) {
            r *= x;
            double add = r - r % 1;
            sb.append(String.valueOf((int) add));
            r -= add;
            if (map.containsKey(r)) {
                map.put(r, map.get(r) + 1);
            } else {
                map.put(r, 1);
            }
        }
        if (sb.charAt(sb.length() - 1) == '0') {
            sb.deleteCharAt(sb.length() - 1);
        }
        return sb.toString();
    }

    static long getMaxBit(long n) {
        return (int) (Math.log(n) / Math.log(2)) + 1;
    }

    static long complement1(long n) {
        int ans = 0;
        int bi = toSysX(n, 2);
        int pow = 1;
        while (bi >= 1) {
            ans += ((bi % 10 + 1) % 2) * pow;
            bi /= 10;
            pow *= 10;
        }
        ans += pow;
        return ans;
    }

    static long complement2(long n) {
        return toSysX(toDecimal(complement1(n), 2) + 1, 2);
    }

    static int toSysX(long decimal, int to) {
        int ans = 0, pow = 1;
        while (decimal >= 1) {
            ans += (decimal % to) * pow;
            pow *= 10;
            decimal /= to;
        }
        return ans;
    }

    static long toDecimal(long n, int x) {
        long ans = 0, pow = 1;
        while (n >= 1) {
            ans += (n % 10) * pow;
            pow *= x;
            n /= 10;
        }
        return ans;
    }

    static long fromTo(long n, int from, int to) {
        return toSysX(toDecimal(n, from), to);
    }

    static String ExtendedLeft(int n) {
        String sn = String.valueOf(n);
        StringBuilder sb = new StringBuilder(sn);
        while (sb.length() != 8 ) {
            sb.insert(0,"0");
        } 
        return sb.toString();
    }
 

    static String floatingPoint(double d) {
        StringBuilder sb = new StringBuilder();
        sb.append(d < 0 ? "1" + " " : "0" + " ");
        String m = fixedpoint(Math.abs(d), 2);
        System.out.println(m);
        int dec = m.indexOf(".") - m.indexOf("1");
        int exp = 127 + dec;
        int expo = toSysX(exp, 2);
        String ex = ExtendedLeft(expo); 
        m = m.substring(m.indexOf("1") + 1);
        sb.append(ex + " ");
        sb.append(m);
        return sb.toString();
    }
